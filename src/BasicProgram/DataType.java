package BasicProgram;

public class DataType {

	public static void main(String[] args) {
		
		//Numeric values
		byte a = 10;
		short b = 20;
		int age = 30;
		long d = 1000;
		
		//Decimal values
		float f = 10.12f;
		double g = 100.200;
		char h = '+';
		boolean i = true;
		String name = "Harry90909090";
		String date = "10-Mar-2022";
		System.out.println("Byte value of a = " + a); //+ is concatenation symbol
		System.out.println("Short value of b = " + b);
		System.out.println("Age of person = " + age);
		System.out.println("Long value of d = " + d);
		System.out.println("float value of f = " + f);
		System.out.println("double value of g = " + g);
		System.out.println("char value of h = " + h);
		System.out.println("boolena value of i = " + i);
		System.out.println("String value of name variable = " + name);
		System.out.println("Today's Date = " + date);
		}
		// 0 1 = 2
		//1Byte = 8 bit 16 32 64 128 256 512 1024 2048

	}
