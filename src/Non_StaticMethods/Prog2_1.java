package Non_StaticMethods;
// Non Static method call from Different class
public class Prog2_1 {
	
	public void add()
	{
		int a=10,b=30,c;
		c=a+b;
		System.out.println("Addition= "+c);
	}
	
	public void sub()
	{
		int a=20,b=5,c;
		c=a-b;
		System.out.println("Subtraction= "+c);
	}
	
	public void mul()
	{
		int a=5,b=8,c;
		c=a*b;
		System.out.println("Multiplication= "+c);
	}

}
