package String_Prog;
// toUpperCase = String Convert Small Letter into Capital Letter..
// toLowerCase = String Convert Capital Letter Into Small Letter..
public class Progr03 {

	public static void main(String[] args) {
		String a = "Naruto Uzumaki";
		
		System.out.println("Capital Letter = "+a.toUpperCase());
		System.out.println("Small Letter = "+ a.toLowerCase());
	}

}
