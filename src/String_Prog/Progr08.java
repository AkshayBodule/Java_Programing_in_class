package String_Prog;
// startWith() = we are checking our String is Start From that input or not..
// endWith() = we are checking our String is Ending that input or not..
public class Progr08 {

	public static void main(String[] args) {
	String a = "I am From Nagpur";
	
	System.out.println(a.startsWith("I"));
	System.out.println(a.startsWith("From"));
	
	System.out.println(a.endsWith("pur"));
	System.out.println(a.endsWith("NagPur"));

	}

}
